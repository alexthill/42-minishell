char	*ft_strrchr(char const *s, int c)
{
	char	*r;

	if (s == 0)
		return (0);
	r = 0;
	while (*s)
	{
		if (*s == (char)c)
			r = (char *)s;
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (r);
}
